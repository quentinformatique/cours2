


public abstract class Utilisateur {
    
     String nom;

    
     String prenom;

    
     String identifiant;

    
     String motDePasse;

    public Utilisateur(String nom, String prenom, String identifiant, String motDePasse) {
        this.nom = nom;
        this.prenom = prenom;
        this.identifiant = identifiant;
        this.motDePasse = motDePasse;
    }

    
    
    

}
